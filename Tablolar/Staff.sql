CREATE TABLE Staff(
staffID INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
firstName NVARCHAR(50) NOT NULL,
lastName NVARCHAR(50) NOT NULL,
address1 NVARCHAR(255) NOT NULL,
address2 NVARCHAR(255) NOT NULL,
countryID TINYINT NOT NULL, 
cityID INT NOT NULL, 
postCode NVARCHAR(5) NOT NULL,
gender CHAR(1) NOT NULL, 
phoneNumber NVARCHAR(15) NOT NULL,
email NVARCHAR(30) NOT NULL,
dateOfBirth DATE NOT NULL,
identityNumber NVARCHAR(11) unique NOT NULL,
positionID TINYINT NOT NULL, 
workPhone NVARCHAR(30) NOT NULL,
dateOfStart DATE NOT NULL,
dateOfExit DATE,
departmentID TINYINT NOT NULL, 
salary FLOAT NOT NULL 
);